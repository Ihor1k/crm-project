export function InputField({
  id,
  label,
  showRequired = false,
  type = "text",
  name,
  value = "",
  placeholder = "",
  icon = "email",
}) {
  const safeId = escapeHtmlAttr(id);
  const safeName = escapeHtmlAttr(name);
  const requiredMark = showRequired
    ? ' <span class="input-field__label-required" aria-hidden="true">*</span>'
    : "";

  const autocomplete =
    type === "password"
      ? "current-password"
      : type === "email"
        ? "email"
        : "on";

  return `
    <div class="input-field">
      <label for="${safeId}" class="input-field__label">${escapeHtml(label)}${requiredMark}</label>
      <div class="input-field__control">
        <span class="input-field__icon">${icons[icon] ?? icons.email}</span>
        <input
          id="${safeId}"
          name="${safeName}"
          type="${escapeHtmlAttr(type)}"
          value="${escapeHtmlAttr(value)}"
          placeholder="${escapeHtmlAttr(placeholder)}"
          autocomplete="${escapeHtmlAttr(autocomplete)}"
          required
        />
      </div>
    </div>
  `;
}
